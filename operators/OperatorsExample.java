package com.corejavaprojects.operators;

public class OperatorsExample {

	public static void main(String[] args) {
		int i = 100;
		System.out.println("Initial Value is " + i);
		System.out.println("Value after using Pre-Increament Oparator ++i is " + ++i);
		System.out.println("Value after using Post-Increament Oparator i++ is " + i++);
		System.out.println("Value after using Pre-Decreament Oparator --i is " + --i);
		System.out.println("Value after using Post-Decreament Oparator i-- is " + i--);
		System.out.println("Final value is " + i);
	}
}